package Simple_Programs;

public class ThreePlusTwoMinus {
	public static void main(String args[])//main() method
	{
     for(int i=7;i<20;)//for-loop
		{
		  System.out.print(i + ",");//printing it
		  i+=3;//adding 3 and storing it
		  System.out.print(i + ",");//print it
		  i-=2;//subtracting 2 and storing it
		}
	}
}